tsSysConfig	equ	#20af	; задать конфигурацию системы:
				; i: A
				;   [2]:    Разрешить(1) / Запретить(0) кеш. Бит копируется в биты 3-0 tsCacheConfig 
				;   [1-0]:  Задать частоту z80:
				;		%00	3.5Mhz
				;		%01	7.0Mhz
				;		%10	14.0Mhz
