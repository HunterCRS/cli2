tsDMActr	equ	#27af	; управление DMA и начало транзакции
				; i: A
				;   [7]:   флаг ~R/W (используется ниже совместно с битами 2-0)
				;   [5-4]: выравнивание адреса источника (5) и получателя (4). После каждого блока данных (бурста):
				;            если значение %0 - адрес (источника/получателя) содержит следующий адрес,
				;	     если значение %1 - младшие часть адреса (8 или 9 бит) инициализируется значением, которое было при запуске транзакции,
				;        а сташая часть адреса, начиная с бита 8/9 увеличивается на 1. Таким образом, все бурсты выравниваются на 256/512 байт.
				;   [3]:   определяет размер выравнивания адреса:
				;            %0 - выравнивание 256 байт (8 младших бит адреса) 
				;            %1 - выравнивание 512 байт (9 младших бит адреса)
				;          для режима Blitter это также определяет количество битов цвета:
				;            %0 - 4 бита на точку, прозрачный цвет №0 из 16 цветов
				;            %1 - 8 бит на точку, прозрачный цвет №0 из 256 цветов
				;   [2-0]: режим работы DMA:
				;		~R/W источник получатель
				;	   %000	  0	-	-	reserved
				;	   %000	  1	-	-	reserved
				;	   %001	  0	RAM	RAM	Копируются данные из одной части памяти (источник) в другую (получатель)
				;	   %001	  1	BLT	RAM	Blitter: пиксель источника заменяет пиксель приемника, если пиксель источника непрозрачный
				;	   %010	  0	SPI	RAM	Копируются данные из интерфейса SPI(источник, SD Card) в область памяти (получатель). Адрес источника игнорируется.
				;	   %010	  1	RAM	SPI	Копируются данные из памяти (источник) в интерфейс SPI(получатель, SD Card). Адрес приемника игнорируется.
				;	   %011	  0	IDE	RAM	Копируются данные из интерфейса IDE(источник, HDD) в область памяти (получатель). Адрес источника игнорируется.
				;	   %011	  1	RAM	IDE	Копируются данные из памяти (источник) в интерфейс IDE(получатель, HDD). Адрес приемника игнорируется.
				;	   %100	  0	FILL	RAM	Заливается область памяти (получатель), словом (#xxxx) расположенным по адресу источника
				;	   %100	  1	RAM	CRAM	Копируются данные из памяти (источник) в CRAM (получатель). Адрес получателя 0-510
				;	   %101	  0	-	-	reserved
				;	   %101	  1	RAM	SFILE	Копируются данные из памяти (источник) в SFILE (получатель). Адрес получателя 0-510
				;	   %110	  0	-	-	reserved
				;	   %110	  1	-	-	reserved
				;	   %111	  0	-	-	reserved
				;	   %111	  1	-	-	reserved

