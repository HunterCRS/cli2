tsMemConfig	equ	#21af	; задать конфигурацию памяти:
				; i: A
				;   [7-6]:  Задать тип блокировки памяти:
				;		%00	512k
				;		%01	128k
				;		%10	Auto
				;		%11	1024k
				;   [3]:    Установить в #0000 страницу ОЗУ(1) или ПЗУ(0)
				;   [2]:    Разрешить(0) / Запретить(1) выбор банки, в зависимости от режима системы (TR-DOS/Basic 48/128)
		                ;           Биты 7-2 для страницы #0000 всегда берутся из битов 7-2 tsRAMPage0.
		                ;           Биты 1-0 зависят от маппинга. Если маппинг отключен, биты 1-0 tsRAMPage0 берутся напрямую из битов 1-0 tsRAMPage0.
		                ;           Если включен маппинг, то биты 1-0 tsRAMPage0 игнорируются, а вместо них 
		                ;           подставляется комбинация, зависящая от текущего состояния:
				;               %00	Service
				;		        %01	DOS
				;		        %10	128
				;		        %11	48
				;   [1]:    Разрешить(1) / Запретить(0) запись с #0000
				;           Для ОЗУ это обеспечивает защиту записи (эмуляция ПЗУ), для ПЗУ это сигнал ~WE на чипе флэша.
				;   [0]:	Копия бита 4 порта #7FFD

